// The lissajous function has two nested loops.
// The outer loop runs for 64 iterations, each producing
// a single frame of the animation. It creates a new 201&201
// image with a palette of two colors, white and black.
// All pixels are initially set to the palette’s zero value
// (the zeroth color in the palette), which we set to white.
// Each pass through the inner loop generates a new image by
// setting some pixels to black. The result is appended, using
// the built-in append function, to a list of frames in anim,
// along with a specified delay of 80ms. Finally the sequence
// of frames and delays is encoded into GIF format and written
// to the output stream out. The typ e of out is io.Writer, which
// lets us write to a wide range of possible destinations

// Reader takes value or data from the stream of the current process
// Writer outputs data or value to the current stream of the process
func lissajous(out io.Writer) {
	const (
		cycles  = 5     // number of complete x oscillator revolutions
		res     = 0.001 // angular resolution
		size    = 100   // image canvas covers [-size..+size]
		nframes = 64    // number of animation frames
		delay   = 8     // delay between frames in 10ms units
	)
	motionDirection := cycles * 2 * math.Pi

	freq := rand.Float64() * 3.0 // relative frequency of y oscillator
	anim := gif.GIF{LoopCount: nframes}
	phase := 0.0 //phase difference

	for i := 0; i < nframes; i++ {
		rect := image.Rect(0, 0, 2*size+1, 2*size+1)
		img := image.NewPaletted(rect, palette)

		for t := 0.0; t < motionDirection; t += res {
			x := math.Sin(t)
			y := math.Sin(t*freq + phase)
			img.SetColorIndex(size+int(x*size+0.5), size+int(y*size+0.5), uint8(i))
		}
		phase += 0.1
		anim.Delay = append(anim.Delay, delay)
		anim.Image = append(anim.Image, img)
	}
	// NB: ignoring encoding errors
	gif.EncodeAll(out, &anim)
}
